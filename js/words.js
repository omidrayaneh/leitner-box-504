window.confirmReset = function() {
    Swal.fire({
        title: 'ریست کردن تمرین‌ها',
        text: 'آیا مطمئن هستید که می‌خواهید همه پیشرفت خود را از بین ببرید؟',
        icon: 'warning',
        showCancelButton: true,
        confirmButtonColor: '#3085d6',
        cancelButtonColor: '#d33',
        confirmButtonText: 'بله، ریست کن',
        cancelButtonText: 'انصراف'
    }).then((result) => {
        if (result.isConfirmed) {
            resetBoxes();
            resetStats();
            Swal.fire(
                'ریست شد!',
                'تمام پیشرفت شما به حالت اول برگشت.',
                'success'
            ).then(() => {
                // بارگذاری مجدد صفحه برای نمایش تغییرات
                window.location.reload();
            });
        }
    });
};

